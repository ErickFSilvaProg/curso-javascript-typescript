/** 
 * Crie variáveis com 'let', não utilise 'var'.
 * Não podemos criar variáveis com palavras reservadas.
 * As variáveis precisam ter nomes significativos.
 * Não podemos iniciar o nome de variável com um número.
 * As variáveis não podem conter espaços ou traços.
 * Criamos variáveis no formato camelCase.
 * As variáveis são case-sensitive.
 * Não podemos redeclarar variáveis com 'let'.
*/


let marido = 'João';
marido = 'Pedro';
let esposa = 'Maria';
let filho = 'Eduardo';

console.log(`${marido} nasceu em 1984.`);
console.log(`Em 2000 ${marido} conheceu ${esposa}.`);
console.log(`${marido} casou-se com ${esposa} em 2012.`);
console.log(`${esposa} teve 1 filho com ${marido} em 2015.`);
console.log(`O filho de ${marido} se chama ${filho}.`);